#include "../push_swap.h"

int	ft_verif_char(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!((str[i] <= '9' && str[i] >= '0') || (str[i] >= 9 && str[i] <= 13)
				|| str[i] == 32 || str[i] == '+' || str[i] == '-'))
			return (-1);
		i++;
	}
	return (0);
}
